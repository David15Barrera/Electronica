    public void listarCat(){
        jcCategoria.removeAllItems();
        List<Productos> listProdu = productos.ListarCat();
        
        for (int i = 0; i < listProdu.size(); i++){
            jcCategoria.addItem(String.valueOf(listProdu.get(i).getCategoria()));
        }
    }
