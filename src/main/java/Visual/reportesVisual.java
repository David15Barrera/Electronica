        public void RowTable(){
    jtVentasReal.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
    jtVentasReal.setRowHeight(20);
    for (int column = 0; column < jtVentasReal.getColumnCount(); column++) {
        TableColumn tableColumn = jtVentasReal.getColumnModel().getColumn(column);
        int preferredWidth = tableColumn.getMinWidth();
        int maxWidth = tableColumn.getMaxWidth();

        for (int row = 0; row < jtVentasReal.getRowCount(); row++) {
            TableCellRenderer cellRenderer = jtVentasReal.getCellRenderer(row, column);
            Component c = jtVentasReal.prepareRenderer(cellRenderer, row, column);
            int width = c.getPreferredSize().width + jtVentasReal.getIntercellSpacing().width;
            preferredWidth = Math.max(preferredWidth, width);

            //  Hemos excedido el ancho máximo, no es necesario verificar otras filas
            if (preferredWidth >= maxWidth) {
                preferredWidth = maxWidth;
                break;
            }
        }
        tableColumn.setPreferredWidth(preferredWidth);
    }
    }
        
public void sumaTotal(){
 double sumatoria1 = 0.0;
DecimalFormat df = new DecimalFormat("#.##");
int totalRow = jtVentasReal.getRowCount();
totalRow -= 1;
for(int i=0; i <= totalRow; i++) {
    sumatoria = Double.parseDouble(String.valueOf(jtVentasReal.getValueAt(i,6)));
    sumatoria1 += sumatoria;
}

Double totalForm = Double.parseDouble(df.format(sumatoria1));
jtTotaldia.setText(String.valueOf(totalForm));
     
}
    
