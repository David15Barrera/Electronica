    public void listarVentas(){
        List<Ventas> ListarUser = reportes.ListRVent();
        modelo = (DefaultTableModel) jtVentasReal.getModel();
        Object[] ob = new Object[7];
        for (int i = 0; i < ListarUser.size(); i++) {
            ob[0] = ListarUser.get(i).getIdVenta();
            ob[1] = ListarUser.get(i).getNomUser()+" "+ ListarUser.get(i).getApeUser();
            ob[2] = ListarUser.get(i).getNomClie()+" "+ ListarUser.get(i).getApelClie();
            ob[3] = ListarUser.get(i).getUbiSucur();
            ob[4] = ListarUser.get(i).getFecha();
            ob[5] = ListarUser.get(i).getTotal();
            ob[6] = ListarUser.get(i).getSubtotal();
            modelo.addRow(ob);
        }
        jtVentasReal.setModel(modelo);
    }
