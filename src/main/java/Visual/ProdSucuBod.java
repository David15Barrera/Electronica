    private void jbAgregarInvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarInvActionPerformed
        //Codigo para actualizar el valor de la cantidad de inventario
         if(txtSucursal.getText().equals("") || txtPrecio.getText().equals("") || txtCodProd.getText().equals("") || jDateChooser1.getDate().equals("")){
            JOptionPane.showMessageDialog(null, "Todo los campos son requeridos");
            }else{
                                 String[] opciones = {"SI","NO"};

                    int n = JOptionPane.showOptionDialog(this,"¿Todo Correcto?","AGREGAR",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE,null,opciones,"");

                    if (n == JOptionPane.YES_OPTION) {
                        //TU CODIGO SI SI
                            String dpi = txtCodProd.getText();
                            int nit = Integer.parseInt(String.valueOf(jComboBox1.getSelectedItem()));
                            String nom = txtPrecio.getText();
                            String fecha = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
                            
                            prod.setIdProd(dpi);
                            prod.setCantidad(Integer.valueOf(nom));
                            prod.setSucurorigin(nit);
                            prod.setFecha(Date.valueOf(fecha));
                            
                            productos.RegistrarInv(prod);
                            JOptionPane.showMessageDialog(null, "Producto Registrado");
                            System.out.println("Agregado");
                    }else if (n == JOptionPane.NO_OPTION) {
                        //TU CODIGO SI NO
                          JOptionPane.showMessageDialog(null, "Registrado Cancelado");
                    }
                    
         }
    }//GEN-LAST:event_jbAgregarInvActionPerformed

