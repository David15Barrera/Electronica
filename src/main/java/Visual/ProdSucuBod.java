public void listarCat(){
        jComboBox1.removeAllItems();
        List<Sucursal> listProdu = sucursal.ListarSucu();
        
        for (int i = 0; i < listProdu.size(); i++){
            jComboBox1.addItem(String.valueOf(listProdu.get(i).getIdSucur()));
        }

    }

